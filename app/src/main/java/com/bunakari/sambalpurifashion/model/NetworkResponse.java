package com.bunakari.sambalpurifashion.model;

public class NetworkResponse {
    public String id;
    public String name;
    public String userappreferralcode;
    public String usernetworkcount;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserappreferralcode() {
        return userappreferralcode;
    }

    public void setUserappreferralcode(String userappreferralcode) {
        this.userappreferralcode = userappreferralcode;
    }

    public String getUsernetworkcount() {
        return usernetworkcount;
    }

    public void setUsernetworkcount(String usernetworkcount) {
        this.usernetworkcount = usernetworkcount;
    }
}
